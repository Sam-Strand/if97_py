@vectorize([float64(float64)], nopython=True, cache=True)
def γ0_τ(tau):
    sum_val = 0.0
    for j in range(len(J0)):
        sum_val += N0[j] * J0[j] * tau ** (J0[j] - 1)
    return sum_val


@vectorize([float64(float64, float64)], nopython=True, cache=True)
def γr_τ(tau, pi):
    sum_val = 0.0
    for j in range(len(nr)):
        sum_val += nr[j] * pi ** Ir[j] * Jr[j] * (tau - 0.5) ** (Jr[j] - 1)
    return sum_val


@vectorize([float64(float64)], nopython=True, cache=True)
def get_π(p):
    return p / 16.53


@vectorize([float64(float64)], nopython=True, cache=True)
def get_τ(t):
    return 540.0 / t


class SteamRegion:
    @staticmethod
    @vectorize([float64(float64, float64)], nopython=True, cache=True)
    def enthalpy_t_p(t, p):
        τ = get_τ(t)
        π = get_π(p)
        return 249224.04 * (γ0_τ(τ) + γr_τ(τ, π))


